class Lallipop {
    public static void unwrap() { System.out.println("Unwrapping the lollipop."); }
    public static void eat() { System.out.println("Eating the lollipop."); }
    public static void checkFlavor() { System.out.println("The flavor is strawberry."); }
    public static void breakStick() { System.out.println("Stick is broken."); }
    public static void throwWrapper() { System.out.println("Wrapper is thrown in dustbin."); }

    public static void taste() { checkFlavor(); }
    public static void melt() { System.out.println("Lollipop is melting."); }
    public static void lick() { System.out.println("Licking the lollipop."); }
    public static void finish() { System.out.println("Lollipop is finished."); }
    public static void buyMore() { System.out.println("Buying more lollipops."); }
}
